<h3>Editer, importer et exporter des gabarits de Gallery</h3>
<p>Pour pouvoir &eacute;diter des gabarits, l&#039;utilisateur doit appartenir &agrave; un groupe avec la permission &#039;Modify Templates&#039;.</p>
<p>Des gabarits pour Gallery peuvent &ecirc;tre t&eacute;l&eacute;charg&eacute;s ind&eacute;pendamment sur la page du <a href="http://dev.cmsmadesimple.org/projects/gallery/">module Gallery</a>. Si la checkbox est coch&eacute;e, l&#039;import d&#039;un gabarit va &eacute;craser tout gabarit existant ayant le m&ecirc;me nom. Notez qu&#039;une mise &agrave; jour du module Gallery n&#039;alt&eacute;rera pas vos gabarits existants.</p>
<p>Si vous voulez cr&eacute;er votre propre gabarit et pouvoir l&#039;exporter, v&eacute;rifiez de bien mettre tous les fichiers n&eacute;cessaires (outre ceux disponibles par d&eacute;faut) dans son propre r&eacute;pertoire au nom du gabarit dans modules/Gallery/templates/. Ces fichiers seront automatiquement inclus dans le fichier XML d&#039;export. Cliquez sur le lien &#039;A propos&#039; pour modifier le num&eacute;ro de version et le texte &#039;A propos&#039;.</p>

<h3>Options globales de Gallery</h3>
<p>Pour modifier les options globales de Gallery, l&#039;utilisateur doit appartenir &agrave; un groupe avec la permission &#039;Modify Site Preferences&#039;.</p>

